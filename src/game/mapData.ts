export const STATIC_MAP_DATA = mapLayout.map(row =>
    [...row].map(c => {
        switch (c) {
            case "#":
                return TILE_TYPE.WALL;
            case "-":
            case "'":
                return TILE_TYPE.FLOOR;
            case "_":
                return TILE_TYPE.ONEWAY;
            default:
                return TILE_TYPE.FLOOR;
        }
    })
);

export function getFoodMap() {
    return mapLayout.map(row =>
        [...row].map(c => c === "'" ? true : false)
    );
}
