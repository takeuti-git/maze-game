export const staticMapData = mapLayout.map(row =>
    [...row].map(c => {
        switch (c) {
            case "#":
                return TileType.Wall;
            case "-":
            case "'":
                return TileType.Floor;
            case "_":
                return TileType.Oneway;
            default:
                return TileType.Floor;
        }
    })
);

export function getFoodMap() {
    return mapLayout.map(row =>
        [...row].map(c => c === "'" ? true : false)
    );
}
